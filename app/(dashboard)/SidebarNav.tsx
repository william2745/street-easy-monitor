export default function SidebarNav() {
  const pathname = usePathname()

  function isActive(href: string) {
    if (href === '/dashboard') return pathname === '/dashboard'
    return pathname.startsWith(href)
  }

  return (
    <nav className="flex-1 px-2 py-2 space-y-px">
      {navItems.map(item => {
        const active = isActive(item.href)
        return (
          <Link
            key={item.href}
            href={item.href}
            className={`flex items-center gap-2 px-2 py-1.5 rounded text-[12px] transition-colors ${
              active
                ? 'bg-emerald-50 text-emerald-700 font-medium'
                : 'text-zinc-500 hover:bg-zinc-50 hover:text-zinc-700'
            }`}
          >
            <svg width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="1.5" strokeLinecap="round" strokeLinejoin="round" className={active ? 'text-emerald-600' : 'text-zinc-400'}>
              <path d={item.icon} />
            </svg>
            {item.label}
          </Link>
        )
      })}
    </nav>
  )
}
